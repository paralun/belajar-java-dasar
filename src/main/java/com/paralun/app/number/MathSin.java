package com.paralun.app.number;

public class MathSin {

    public static void main(String[] args) {
        double x = 100;
        double degress = 45.0;
        double radians = Math.toRadians(degress);
        
        System.out.println(Math.sin(x));
        System.out.println(Math.sin(radians));
    }
    
}
