package com.paralun.app.number;

public class MathAbs {

    public static void main(String[] args) {
        int i = -7, ii = 7;
        double d = -100, dd = 100;
        float f = -90, ff = 90;
        
        System.out.println("i = " + Math.abs(i) + " dan ii = " + Math.abs(ii));
        System.out.println("d = " + Math.abs(d) + " dan ii = " + Math.abs(dd));
        System.out.println("f = " + Math.abs(f) + " dan ii = " + Math.abs(ff));
        
    }
    
}
